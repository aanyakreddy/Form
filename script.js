let day=prompt("Enter a Color of the Rainbow");
switch(day){
    case "red":console.log("Sunday")
    break;
    case "orange":console.log("Monday")
    break;
    case "yellow":console.log("Tuesday")
    break;
    case "green":console.log("Wednesday")
    break;
    case "blue": console.log("Thursday")
    break;
    case "purple":console.log("Friday")
    break;
    case "indigo":console.log("Saturday")
    break;
    default:console.log("Invalid Option")
}
